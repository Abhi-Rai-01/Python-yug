# round() function:-
# - returns rounded version of specified number
# syntax:- round(number,digits)
# where number:- number specified for rounding
#     digits:- number of decimals to use (optional argument and default is 0)

num = 5.6712
print(round(num,0))
print(round(num,1))
print(round(num,2))
print(round(num,3))
print(round(num,4))
print(round(num,5))
print(round(num,6))